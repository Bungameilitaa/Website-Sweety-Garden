  LANGKAH - LANGKAH :
  <br><br>
  - Lubangi bagian tengah tutup botol menggunakan obeng. Sesuaikan besar lubang dengan diameter selang plastik.
  <br><br>
  - Masukkan ujung selang plastik ke dalam tutup botol
  <br><br>
  - Oleskan lem akuarium di sisi dalam tutup botol. Ratakan lem hingga menutup celah-celah di antara selang plasktik dan tepi lubang. Tunggu hingga lem kering.
